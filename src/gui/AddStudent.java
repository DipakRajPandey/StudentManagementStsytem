  private boolean input(){
        boolean check=false;
       if(Phone.getText().length()==10){
           check=true;
       }
      if(txtId.getText().isEmpty()||(txtName.getText().trim().isEmpty()==txtName.getText().matches("[a-zA-Z]*[a-zA-Z]+[a-zA-Z]*"))||
         (  FatherName.getText().trim().isEmpty()==FatherName.getText().matches("[a-zA-Z]*[a-zA-Z]+[a-zA-Z]*"))||
           City.getText().trim().isEmpty()||(Phone.getText().isEmpty()==check)||
            StdClass.getText().isEmpty()|| txtBlood.getText().isEmpty()){
            return false;
      }
        return true;
} 
