    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
            
            boolean result=input();
       if(!result){
            JOptionPane.showMessageDialog(null,"Please Enter all details properly");
            return;
       }
       
        try{
          

            int stdId=Integer.parseInt(txtId.getText());
            String stdName=txtName.getText().trim();
            String stdFather=FatherName.getText().trim();
            String stdBlood=txtBlood.getText();
            long stdPhone=Long.parseLong(Phone.getText());
            String stdCity=City.getText().trim();
            int stdclass=Integer.parseInt(StdClass.getText());
      //JOptionPane.showMessageDialog(null,stdId+"\n"+stdName+"\n"+stdFather+"\n"+stdBlood+"\n"+stdPhone+"\n"+stdCity+"\n"+stdclass,"Fail",JOptionPane.ERROR_MESSAGE);
            Student e=StudentDao.findStudentById(stdId);
             if(e!=null)
            {
                 JOptionPane.showMessageDialog(null,"id already taken","Error",JOptionPane.ERROR_MESSAGE);
            return; 
            }      
      Student s=new Student(stdId,stdName,stdFather,stdBlood,stdPhone,stdCity,stdclass);
           boolean response=StudentDao.addStudent(s);
            if(response){
            JOptionPane.showMessageDialog(null,"Date is successfully added");                           
         }
        else{
                  JOptionPane.showMessageDialog(null,"Record not added  ","Fail",JOptionPane.ERROR_MESSAGE);
             }
        }
       catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB", "Error",JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        } 
        catch(NumberFormatException ex){
             JOptionPane.showMessageDialog(this, "Enter numerical Data","Error",JOptionPane.ERROR_MESSAGE);
               ex.printStackTrace();
         }
    }//GEN-LAST:event_btnAddActionPerformed

